// Ground truth for the holdings-image OCR eval (`scripts/eval/ocr.ts`).
//
// Fixtures are committed as SVG (`fixtures/ocr/*.svg`) and rendered to PNG at
// eval time via sharp — no binaries in the repo, no browser dependency. The
// data is synthetic-but-realistic: real popular Thai fund codes (fair-use,
// illustrative) with invented units/NAV/cost/value, NOT the owner's holdings.
//
// Two layouts cover the two real-world journeys the extractor targets:
//   - holdings-table  → DETAIL view: units + NAV + avg cost all shown.
//   - holdings-cards  → SUMMARY view: only value + P/L (the common Thai-app
//     case where units are absent and the app must derive them from NAV).
//
// `hard: true` cases are re-rendered downscaled + low-quality JPEG by the
// runner to stress digit fidelity (the failure mode that separated models in
// the original A/B — flash-lite lost NAV/avgCost on the hard variant).

export interface ExpectedRow {
  ticker: string;
  units?: number;
  nav?: number;
  avgCost?: number;
  value?: number;
  pl?: number;
  costTotal?: number;
  // transaction-journey fields (extractTransactionRows)
  kind?: string; // buy | sell | dividend | …
  tradeDate?: string; // ISO YYYY-MM-DD (Buddhist-era year − 543)
  amount?: number;
}

/**
 * A chart/factsheet visual-Q&A probe (the `visual` journey). The model reads the
 * image via the production examine_image tool; the answer is scored by checking
 * it CONTAINS every `expect` token (case-insensitive). Synthetic fixtures print
 * precise values, so this is deterministic — no LLM judge needed. This is the
 * gate (G2) that decides whether `VISION_CHAT_ESCALATE_MODELS` earns its cost.
 */
export interface VisualQuestion {
  q: string;
  /** Substrings the correct answer must all contain (e.g. ["-8.2"], ["2024"]). */
  expect: string[];
}

export interface OcrTestCase {
  /** Which extractor/journey this case exercises (default holdings). */
  journey?: "holdings" | "txn" | "visual";
  /** SVG fixture filename under `fixtures/ocr/` (rendered to PNG at run time). */
  svg?: string;
  /**
   * OR a raw image filename, resolved against EVAL_OCR_DIR — for running the
   * harness on REAL screenshots locally (personal data, never committed; supply
   * a gitignored `<EVAL_OCR_DIR>/ground-truth.json` with these cases). Real
   * images are used as-is (no synthetic "hard" degradation tier).
   */
  image?: string;
  /** Layout / journey label for the scorecard. */
  label: string;
  /** Which numeric fields the image actually shows (only these are scored). */
  fields: Array<keyof Omit<ExpectedRow, "ticker">>;
  rows: ExpectedRow[];
  /** For the `visual` journey: chart/factsheet questions with checkable answers. */
  questions?: VisualQuestion[];
  /**
   * Trap notes — non-holding text in the image the extractor must NOT turn into
   * a row or a field. Any extra row beyond `rows` counts as a hallucination.
   */
  traps?: string;
}

